uint32_t layer_state_set_keymap(uint32_t state) {
  switch (biton32(state)) {
    case _RAISE:
      rgblight_setrgb (0x00, 0x00, 0xFF);
      break;
    default: //  for any other layers, or the default layer
      rgblight_mode(28);
      break;
  }
  return state;
}
